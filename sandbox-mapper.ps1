# --- Параметры запуска ---
# Источник и целевая директория задаются как параметры скрипта.
# Это позволяет переиспользовать скрипт для разных проектов без изменения кода.
param (
    [string]$Source = 'D:\repos\public_repositories\FastApiFoundry-Docker',
    [string]$Target = 'D:\FastApiFoundry-Docker-Sandbox'
)

# --- Глобальная политика обработки ошибок ---
# Stop = любые ошибки вызывают исключение и переход в catch-блок.
$ErrorActionPreference = 'Stop'


function Sync-Project {
    <#
    .SYNOPSIS
        Зеркалирование исходной директории в целевую папку через robocopy.

    .DESCRIPTION
        Выполнение полной синхронизации структуры каталогов.
        Использование режима /MIR позволяет поддерживать целевую директорию
        в идентичном состоянии с источником, включая удаление лишних файлов.
        Это критично для предотвращения конфликтов версий в песочнице.

    .PARAMETER Source
        Путь к исходной директории проекта на хосте.

    .PARAMETER Target
        Путь к целевой директории для монтирования песочницы.

    .OUTPUTS
        [bool] — True при успешной синхронизации, False при критической ошибке.
    #>

    param (
        [string]$Source,
        [string]$Target
    )

    try {

        # ---------------------------------------------------------------------
        # robocopy — основной механизм синхронизации
        # ---------------------------------------------------------------------
        # /MIR
        #   Зеркалирование: эквивалент /E + /PURGE.
        #   Удаляет в Target файлы, которых нет в Source.
        #
        # /XD (exclude directories)
        #   Исключает каталоги, которые не должны попадать в песочницу:
        #   - venv            → Python виртуальные окружения
        #   - .git            → метаданные git
        #   - __pycache__     → байткод Python
        #   - node_modules    → зависимости npm (обычно пересоздаются)
        #   - .mypy_cache     → кеш статического анализа mypy
        #   - .pytest_cache   → кеш тестового раннера pytest
        #
        # /XF (exclude files)
        #   Исключает временные и бинарные файлы:
        #   - *.pyc, *.pyo    → байткод Python
        #   - *.log           → лог-файлы (не переносятся в sandbox)
        #
        # /R:1
        #   Количество повторных попыток при ошибке копирования = 1.
        #
        # /W:1
        #   Время ожидания между попытками = 1 секунда.
        #
        # /NFL /NDL /NJH /NJS
        #   Уменьшение verbosity:
        #   - NFL → no file list
        #   - NDL → no directory list
        #   - NJH → no job header
        #   - NJS → no job summary
        #   Используется для "чистого" вывода в консоль.
        # ---------------------------------------------------------------------

        # ПОЧЕМУ ROBОCOPY:
        #   В отличие от Copy-Item, robocopy работает на уровне файловой системы
        #   более эффективно, корректно обрабатывает длинные пути и позволяет
        #   реализовать инкрементальное обновление (копируются только измененные файлы).
        #   Флаг /MIR гарантирует, что в песочнице не останется "хвостов" от старых
        #   удаленных модулей.

        robocopy $Source $Target `
            /MIR `
            /XD venv .git __pycache__ node_modules .mypy_cache .pytest_cache `
            /XF *.pyc *.pyo *.log `
            /R:1 /W:1 `
            /NFL /NDL /NJH /NJS

        # Обработка специфичных кодов завершения robocopy:
        # 0–7   → успех (с различными предупреждениями)
        # >= 8  → ошибка
        $code = $LASTEXITCODE

        return ($code -le 3)
    }
    catch {

        # Обработка исключений уровня системы (доступ к файлам, права).
        # Логирование ошибки для последующей диагностики.
        Write-Host "⚠️ Sync-Project: $_"
        return $false
    }
}


# --- main ---

# Уведомление о начале процесса синхронизации
Write-Host '🔄 Sync started...'

# Вызов функции синхронизации
$result = Sync-Project -Source $Source -Target $Target

# Формирование финального статуса:
# - успех: все файлы синхронизированы без критических ошибок robocopy
# - ошибка: либо robocopy вернул критический код, либо произошёл exception
if ($result) {
    Write-Host '✅ Sync completed'
} else {
    Write-Host '❌ Sync failed'
}